package practicaobligatoria;

/**
 *
 * @author albam
 */
public class ConstanteClass {
    private String nombre;
    private String valor;
    
    public ConstanteClass(String nombre, String valor){
        this.nombre = nombre;
        this.valor = valor;
    }

    public String getNombre() {
        return nombre;
    }

    public String getValor() {
        return valor;
    }
    
    
}
